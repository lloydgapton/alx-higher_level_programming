#!/usr/bin/python3
def add(a, b):
    """My addition function
    Args:
    a: first integer
    b: second integer

    Returns:
    The return value a + b
    """
    return (a + b)

def sub(a, b):
    """My subtraction function
    Args:
    a:first integer
    b:second integer

    Returns:
    The return value, a * b
    """
    return int(a - b)

def mul(a, b):
    """My multiplaiucation function

    Args:

    a: first integer
    b: second integer

    Return:
    the return value . a * b
    """
    return (a * b)

def div(a, b):
    """My division function

    Args:
    a: first integer
    b: second integer

    Returns:
    The return value. a / b
    """
    return int(a / b)
